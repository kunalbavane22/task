package com.model;

public class Teacher {
	private String tname;
	private String taddress;
	private long tcontact;
	private float tsalary;
	private int tdob;
	private String tbloodgroup;
	private String temail;
	public String getTname() {
		return tname;
	}
	public void setTname(String tname) {
		this.tname = tname;
	}
	public String getTaddress() {
		return taddress;
	}
	public void setTaddress(String taddress) {
		this.taddress = taddress;
	}
	public long getTcontact() {
		return tcontact;
	}
	public void setTcontact(long tcontact) {
		this.tcontact = tcontact;
	}
	public float getTsalary() {
		return tsalary;
	}
	public void setTsalary(float tsalary) {
		this.tsalary = tsalary;
	}
	public int getTdob() {
		return tdob;
	}
	public void setTdob(int tdob) {
		this.tdob = tdob;
	}
	public String getTbloodgroup() {
		return tbloodgroup;
	}
	public void setTbloodgroup(String tbloodgroup) {
		this.tbloodgroup = tbloodgroup;
	}
	public String getTemail() {
		return temail;
	}
	public void setTemail(String temail) {
		this.temail = temail;
	}

}
